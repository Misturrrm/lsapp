<h3><strong>Bachelor of Science (B.Sc.) in Information Systems</strong></h3>
<p>The Information Systems program provides students with a solid foundation in information technology principles and practice. The emphasis is on applications of information technology rather than the computer and/or mechanisms per se. The core courses for the information systems major include programming, computer architecture, operating systems, data communication, systems analysis and design, database applications, and project management. Students also take courses in statistics, operations management, IT organizations and financial systems. The Information Systems program also provides the opportunity for students to combine their technical IT courses with general businees requirements from the School of Business and Entrepreneurship.</p>
<br> 
<p>Each graduate of the program should be able to analyze, design and implement high quality software solutions to 'real life' problems; orally present developed software projects; develop software solutions completely observing professional and engineering/central ethical obligations; function effectively in team-oriented cooperative work; pursue independent continuous learning; and have a solid background in basic IS Knowledge.
<br>
<br>
<p>There are five(5) main concentration areas in the Information Systems major : Applied Networking, Database and Web Database, Information Security and Assurance, Management Information Systems and Software Applications Development.</p>
<br>
<p>Students may also decide to pursue a general B.Sc. degree in Information Systems with no concentration by selecting appropriate courses of interest from the concentration areas. The major is designed to prepare students for professional careers in the discipline of Information Systems/Technology. Through required and elective courses and a concentration, students will be prepared for career positions such as:
<ul>
<li>Application Programmers</li>
<li>System Analysts</li>
<li>Software Analysts</li>
<li>Network Analysts</li>
<li>Security Analysts</li>
<li>Information Security Professional</li>
<li>Database Analysts</li>
<li>Database Administrator</li>
<li>Network Administrator</li>
<li>IT Project Manager</li>
</ul>
